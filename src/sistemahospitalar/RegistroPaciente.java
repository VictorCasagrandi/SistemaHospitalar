    private void cad_pacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cad_pacienteActionPerformed
        Paciente paciente = new Paciente();
        PacienteDAO pacienteDAO = new PacienteDAO();
        
        paciente.setNome(txtNome.getText());
        paciente.setRg(txtRg.getText());
        paciente.setCpf(txtCpf.getText());
        paciente.setnSus(txtCartaoSus.getText());
        paciente.setSexo(txtSexo.getText());
        paciente.setEtinia(txtEtinia.getText());
        paciente.setPeso(txtPeso.getText());
        paciente.setAltura(txtAltura.getText());
        paciente.setIdade(txtIdade.getText());
        
        pacienteDAO.adiciona(paciente);
        
        JOptionPane.showMessageDialog(null, "Paciente cadastrado com sucesso!");
    }//GEN-LAST:event_cad_pacienteActionPerformed

